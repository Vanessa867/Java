package polimorfismo;

public class operações {
	double soma(double a,double b) {
		return a + b;
	}
double multiplicar (double a, double b) {
		return a * b;
	}
	double subitrair(double a, double b) {
		return a - b;
	}
	double dividir (double a, double b) {
		return a/b;
	}
	double soma(double a, double b, double c) {
		return a+b+c;
	}
	double multiplicar (double a, double b, double c) {
		return a*b*c;
	}
	double subitrair(double a, double b, double c) {
		return a -b-c;
	}
	double dividir(double a, double b, double c) {
		return a/b/c;
	}
}
